public class App {
    public static void main(String[] args) throws Exception {
        // AND
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(~5);
        System.out.println(5 << 6);
        System.out.println(5 >> 1);
    }
}
